package CollectionFrameWork.Map;

import java.util.HashMap;

public class HashMapExample {


    public static void main(String[] args) {
        HashMap hm = new HashMap();
        hm.put(0 ,"10");
        hm.put(1,"suman singh");
        hm.put(2,2);
        hm.put(3,2.2);
        hm.put(4,false);
        hm.put(5,null);
        System.out.println("HashMap = " + hm);

    }
}
